void tft_send_data(tft_t *tft, uint8_t data)
{
    stm_lib_write_gpio(tft->dc_port, tft->dc_pin, 1);
    stm_lib_spi_tx(tft->spi, data);
    //stm_lib_spi_tx_dma(tft->spi, data, &tft->mem);
}

void tft_send_cmd(tft_t *tft, uint8_t cmd)
{
    stm_lib_write_gpio(tft->dc_port, tft->dc_pin, 0);
    stm_lib_spi_tx(tft->spi, cmd);
    //stm_lib_spi_tx_dma(tft->spi, cmd, &tft->mem);
}

void tft_cs_high(tft_t *tft)
{
    stm_lib_write_gpio(tft->cs_port, tft->cs_pin, 1);
}

void tft_cs_low(tft_t *tft)
{
    stm_lib_write_gpio(tft->cs_port, tft->cs_pin, 0);
}

void tft_init(tft_t *tft, uint8_t spi, uint8_t reset_port, uint8_t reset_pin, uint8_t dc_port, uint8_t dc_pin, uint8_t cs_port, uint8_t cs_pin)
{
    tft->spi = spi;
    tft->reset_port = reset_port;
    tft->reset_pin = reset_pin;
    tft->dc_port = dc_port;
    tft->dc_pin = dc_pin;
    tft->cs_port = cs_port;
    tft->cs_pin = cs_pin;

    stm_lib_config_gpio(reset_port, reset_pin, MODE_OUTPUT_50_GP_PP);
    stm_lib_config_gpio(dc_port, dc_pin, MODE_OUTPUT_50_GP_PP);
    stm_lib_config_gpio(cs_port, cs_pin, MODE_OUTPUT_50_GP_PP);

    stm_lib_write_gpio(reset_port, reset_pin, 0);
    delayms(20);
    stm_lib_write_gpio(reset_port, reset_pin, 1);
    delayms(20);

    tft_cs_low(tft);
    tft_send_cmd(tft, 0x11);
    delayms(10);
    tft_send_cmd(tft, 0x3A);
    tft_send_data(tft, 0x05);
    tft_send_cmd(tft, 0x36);
    tft_send_data(tft, 0x14);
    tft_send_cmd(tft, 0x29);
    tft_cs_high(tft);
}

void tft_set_addr_window(tft_t *tft, uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1)
{
    tft_cs_low(tft);
    tft_send_cmd(tft, ST_CASET); // Column addr set
    tft_send_data(tft, 0x00);
    tft_send_data(tft, x0); // XSTART
    tft_send_data(tft, 0x00);
    tft_send_data(tft, x1);      // XEND
    tft_send_cmd(tft, ST_RASET); // Row addr set
    tft_send_data(tft, 0x00);
    tft_send_data(tft, y0); // YSTART
    tft_send_data(tft, 0x00);
    tft_send_data(tft, y1);      // YEND
    tft_send_cmd(tft, ST_RAMWR); // write to RAM
    tft_cs_high(tft);
}

void tft_fill_rect(tft_t *tft, int16_t x, int16_t y, int16_t w, int16_t h, uint16_t color)
{
    uint8_t hi = color >> 8, lo = color;

    if ((x >= ST_WIDTH) || (y >= ST_HEIGHT))
    {
        return;
    }
    if ((x + w - 1) >= ST_WIDTH)
    {
        w = ST_WIDTH - x;
    }
    if ((y + h - 1) >= ST_HEIGHT)
    {
        h = ST_HEIGHT - y;
    }    
    
    tft_set_addr_window(tft, x, y, x + w - 1, y + h - 1);

    tft_cs_low(tft);
    for (y = h; y > 0; y--)
    {
        for (x = w; x > 0; x--)
        {
            tft_send_data(tft, hi);
            tft_send_data(tft, lo);
        }
    }
    tft_cs_high(tft);
}

void tft_write_char(tft_t* tft, char c, uint16_t tcolor, uint16_t bcolor)
{
    uint32_t i, j, b;

    tft_cs_low(tft);
    for (i = 0; i < 10; i++) 
    {
        b = fdata[(c - 32) * 10 + i];

        for (j = 0; j < 7; j++) 
        {
            if ((b << j) & 0x8000)  
            {
                tft_send_data(tft, tcolor >> 8);
                tft_send_data(tft, tcolor & 0xFF);
            }
            else 
            {
                tft_send_data(tft, bcolor >> 8);
                tft_send_data(tft, bcolor & 0xFF);
            }
        }
    }
    tft_cs_high(tft);
}

void tft_write_string(tft_t* tft, uint8_t x, uint8_t y, char* str, uint16_t tcolor, uint16_t bcolor)
{
    uint8_t _x = x;
    uint32_t i = 0;
    while (*str != 0)
    {
        tft_set_addr_window(tft, _x, y, 6+_x, 9+y);
        tft_write_char(tft, *str, tcolor, bcolor);
        _x += 7;
        str++;
        i++;
        if (i > 17)
        {
            i = 0;
            y += 10;
            _x = x;
        }
    }
}

void tft_write_num(tft_t* tft, uint8_t x, uint8_t y, int num, uint16_t tcolor, uint16_t bcolor) {
	char buffer[16];
	memset(buffer, 0, 16);
	sprintf(buffer, "%d", num);
	tft_write_string(tft, x, y, buffer, tcolor, bcolor);
}

void tft_new_bar(tft_t *tft, uint16_t barcolor, uint8_t ypos)  {

	uint8_t lowcolor = barcolor;
	uint8_t hicolor  = (barcolor >> 8);

	tft_set_addr_window(tft, 0, ypos, ST_WIDTH, 15+ypos);
	tft_cs_low(tft);
	for (int i = 0; i < ST_WIDTH; i++) {
		tft_send_data(tft, hicolor);
		tft_send_data(tft, lowcolor);
	}
	tft_cs_high(tft);
	tft_set_addr_window(tft, 0, ypos, 0, 15+ypos);
	tft_cs_low(tft);
	for (int i = 0; i < 15; i++) {
		tft_send_data(tft, hicolor);
		tft_send_data(tft, lowcolor);
	}
	tft_cs_high(tft);
	tft_set_addr_window(tft, 0, ypos+15, ST_WIDTH, 15+ypos);
	tft_cs_low(tft);
	for (int i = 0; i < ST_WIDTH; i++) {
		tft_send_data(tft, hicolor);
		tft_send_data(tft, lowcolor);
	}
	tft_cs_high(tft);
	tft_set_addr_window(tft, ST_WIDTH, ypos, ST_WIDTH, 15+ypos);
	tft_cs_low(tft);
	for (int i = 0; i < 15; i++) {
		tft_send_data(tft, hicolor);
		tft_send_data(tft, lowcolor);
	}
	tft_cs_high(tft);
}

